namespace Choop.Compiler.ChoopModel
{
    /// <summary>
    /// Specifies the possible data types.
    /// </summary>
    enum DataType
    {
        /// <summary>
        /// Indicates an object type.
        /// </summary>
        Object,
        /// <summary>
        /// Indicates the string type.
        /// </summary>
        String,
        /// <summary>
        /// Indicates the number type.
        /// </summary>
        Number,
        /// <summary>
        /// Indicates the boolean type.
        /// </summary>
        Boolean
    }
}
